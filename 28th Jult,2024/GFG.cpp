class Solution {
  public:

    string removeDups(string str) {
        // Your code goes here
        string ans="";
        map<char,bool>mp;
        for(char i:str){
            if(mp[i]){
                continue;
            }else{
                ans+=i;
                mp[i]=true;
            }
        }
        return ans;
    }
};

